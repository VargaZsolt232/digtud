<li><b>Anomáliák:</b> Az AI modellek néha nem képesek pontosan reprodukálni a valóságot, így előfordulhatnak anomáliák, mint például a nem megfelelően arányos testrészek, furcsa árnyékok vagy a háttérben lévő zavaró elemek.</li>
